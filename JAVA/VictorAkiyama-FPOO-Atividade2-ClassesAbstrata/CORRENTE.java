package Contapackage;

public class CORRENTE extends CONTA{
	
	@Override
	public double Transferir() {
		return this.dinheiro = dinheiro;
	}
	public double Depositar() {
		return this.dinheiro + dinheiro;
	}
	public double Sacar() {
		return this.dinheiro - dinheiro;
	}
	public double Saldo() {
		return this.dinheiro;
	}
}
