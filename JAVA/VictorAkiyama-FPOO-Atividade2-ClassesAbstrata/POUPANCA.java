package Contapackage;

public class POUPANCA extends CONTA {
	
	@Override
	public double Transferir() {
		return this.dinheiro = dinheiro;
	}
	public double Depositar() {
		return this.dinheiro + dinheiro;
	}
	public double Sacar() {
		return this.dinheiro - dinheiro;
	}
	public double Saldo() {
		return this.dinheiro = dinheiro * 1.005;
	}
}
